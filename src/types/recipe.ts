import { IIngredient } from './ingredient';

// Это связка “рецепт ↔ ингредиент” (join-таблица / связь многие-ко-многим) + данные про количество:
export interface IRecipeIngredient {
  id: string; // уникальный id этой связи
  ingredientId: string; // id ингредиента
  quantity: number; // количество этого ингредиента в рецепте (число, в единицах ингредиента)
  ingredient: IIngredient; // вложенный объект ингредиента (чтобы сразу иметь имя/единицы/цену и т.д.)
}

export interface IRecipe {
  id: string;
  name: string;
  description: string | null;
  imageUrl?: string | null;
  ingredients: IRecipeIngredient[]; // список ингредиентов в рецепте (не просто строки, а связи)
}

/*
    Ингредиенты живут отдельно (IIngredient)
    Рецепт хранит список “строк рецепта” (IRecipeIngredient)
    Каждая “строка рецепта” знает какой ингредиент и сколько
*/

/*
1) IIngredient — это “карточка товара/ингредиента” (как в магазине/на складе)

Например:
    id: "ing_1"
    name: "Молоко"
    category: "Молочные"
    unit: "мл" (или "л", "г", "шт")
    pricePerUnit: 0.04 (например 0.04$ за 1 мл, или 40$ за 1 л — как ты выберешь)
    description: "2.5% жирности"
    Это справочник ингредиентов — один раз завёл “Молоко”, и используешь в 100 рецептах.

2) IRecipe — это сам рецепт (блюдо)
    Например: “Омлет”
        id: "rec_1"
        name: "Омлет"
        description: "Быстрый завтрак на сковороде"
        imageUrl: "..." (опционально)
        ingredients: [...] (список того, что нужно)  
        
        
3) IRecipeIngredient — это “строчка в рецепте”

    Типа как в листе покупок/техкарте:

    ✅ “Для омлета нужно 2 яйца”
    ✅ “Для омлета нужно 100 мл молока”

То есть это связка: рецепт ↔ ингредиент + quantity.

Пример одной строки:
    id: "ri_1" (id именно этой строки/связки)
    ingredientId: "ing_egg" (какой ингредиент)
    quantity: 2 (сколько)
    ingredient: {...объект яйца...} (чтобы сразу показать имя, unit, цену и т.д.)
*/

/*
    Почему тут есть и ingredientId, и ingredient?
        ingredientId нужен для хранения связи в базе (быстро, компактно).
        ingredient нужен для удобства на фронте (чтобы сразу вывести “Яйцо”, “шт”, “цена”, не делая отдельно запрос).
*/
